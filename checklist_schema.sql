-- 3. Functions
CREATE OR REPLACE FUNCTION public.calculate_delay_if_late()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $function$
BEGIN
    IF NEW.submission_date IS NOT NULL 
       AND NEW.planned_date IS NOT NULL 
       AND NEW.submission_date > NEW.planned_date THEN
        NEW.delay = NEW.submission_date - NEW.planned_date;
    END IF;
    RETURN NEW;
END;
$function$;

CREATE OR REPLACE FUNCTION public.calculate_dynamic_delay()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $function$
BEGIN
    IF NEW.task_start_date IS NOT NULL AND NEW.submission_date IS NOT NULL THEN
        NEW.delay = NEW.submission_date - NEW.task_start_date;
    END IF;
    RETURN NEW;
END;
$function$;

