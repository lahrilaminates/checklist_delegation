-- 7. Data Seed
INSERT INTO public.working_day_calender (working_date, day, week_num, month)
SELECT 
    gs::date AS working_date,
    CASE 
        WHEN EXTRACT(DOW FROM gs)::int = 0 THEN 'रवि'
        WHEN EXTRACT(DOW FROM gs)::int = 1 THEN 'सोम'
        WHEN EXTRACT(DOW FROM gs)::int = 2 THEN 'मंगल'
        WHEN EXTRACT(DOW FROM gs)::int = 3 THEN 'बुध'
        WHEN EXTRACT(DOW FROM gs)::int = 4 THEN 'गुरु'
        WHEN EXTRACT(DOW FROM gs)::int = 5 THEN 'शुक्र'
        WHEN EXTRACT(DOW FROM gs)::int = 6 THEN 'शनि'
    END AS day,
    EXTRACT(WEEK FROM gs)::int AS week_num,
    EXTRACT(MONTH FROM gs)::int AS month
FROM generate_series(
    '2026-04-22'::date,
    '2027-04-22'::date,
    '1 day'
) AS gs;