    END IF;
    RETURN NEW;
END;
$function$;

CREATE OR REPLACE FUNCTION public.secure_login(
    input_username text, 
    input_password text
)
RETURNS TABLE(
    id bigint, 
    user_name text, 
    role text, 
    user_access text, 
    status text, 
    email_id text, 
    number text, 
    employee_id text
)
LANGUAGE plpgsql
SECURITY DEFINER
AS $function$
BEGIN
    RETURN QUERY
    SELECT 
        u.id::BIGINT,
        u.user_name::TEXT, 
        u.role::TEXT,
        u.user_access::TEXT, 
        u.status::TEXT,
        u.email_id::TEXT,
        u.number::TEXT,
        u.employee_id::TEXT
    FROM users u
    WHERE (u.user_name = input_username OR u.email_id = input_username)
        AND u.password = crypt(input_password, u.password);
END;
$function$;

