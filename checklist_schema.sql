    RETURN QUERY EXECUTE format(
        'SELECT DISTINCT %I::text FROM dropdown_options WHERE %I IS NOT NULL ORDER BY 1', 
        column_name, 
        column_name
    );
END;
$function$;

CREATE OR REPLACE FUNCTION public.hash_password()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $function$
BEGIN
    -- Only hash if it's a new row or the password has changed
    -- and it doesn't look like a hash already (simple check)
    IF TG_OP = 'INSERT' OR (TG_OP = 'UPDATE' AND NEW.password <> OLD.password) THEN
        -- Only hash if the string doesn't start with '$2a$' (a common bcrypt/blowfish prefix)
        IF NEW.password NOT LIKE '$2a$%' THEN
            NEW.password = crypt(NEW.password, gen_salt('bf', 10));
        END IF;
    END IF;
    RETURN NEW;
END;
$function$;

CREATE OR REPLACE FUNCTION public.increment_color_code_for()
